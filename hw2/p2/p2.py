def step(
        model_output: torch.FloatTensor,
        timestep: int,
        sample: torch.FloatTensor,
        eta: float = 0.0,
    ):
        t = timestep
        delta_t = T // num_step
        alpha_prod_t = alphas_cumprod[t]
        alpha_prod_t_prev = alphas_cumprod[t - delta_t] if t - delta_t >= 0 else torch.tensor(1.0)

        # "predicted x_0" of formula (15) from https://arxiv.org/pdf/2006.11239.pdf
        pred_original_sample = (sample - (1 - alpha_prod_t).sqrt() * model_output) / alpha_prod_t.sqrt()

        # Clamp "predicted x_0"
        pred_original_sample = pred_original_sample.clamp(-1, 1)

        # DDIM https://arxiv.org/pdf/2010.02502.pdf
        epsilon = model_output
        var_t = ((1 - alpha_prod_t_prev) /(1 - alpha_prod_t)) * (1 - alpha_prod_t / alpha_prod_t_prev)
        direct = (1 - alpha_prod_t_prev - eta * eta * var_t) ** 0.5

        # Compute the next sample in the reverse process
        sample_next = pred_original_sample * alpha_prod_t_prev ** 0.5 + epsilon * direct

        ran_noise = torch.randn(model_output.shape).to(device)
        std = var_t ** (0.5) * eta * ran_noise
        sample_next = sample_next + std

        return sample_next

