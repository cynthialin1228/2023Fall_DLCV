"""# 2. Interpolation"""

def slerp(vector1, vector2, alpha):
    normalized_v1 = vector1 / torch.norm(vector1)
    normalized_v2 = vector2 / torch.norm(vector2)

    angle = torch.acos((normalized_v1 * normalized_v2).sum())
    sin_angle = torch.sin(angle)

    scale_vector1 = torch.sin((1 - alpha) * angle) / sin_angle
    scale_vector2 = torch.sin(alpha * angle) / sin_angle

    interpolated_vector = scale_vector1 * vector1 + scale_vector2 * vector2
    return interpolated_vector

noise_vectors = [load_noise_vector(f'/content/hw2_data/face/noise/0{i}.pt') for i in range(2)]

# Initialize alpha values
alpha_values = torch.arange(0, 1.1, 0.1, device='cuda:0')  # alpha values from 0 to 1 with step of 0.1

# interpolate
for alpha in alpha_values:
    sample_interpolated = slerp(noise_vectors[0], noise_vectors[1], alpha)
    generate_image(model, sample_interpolated, alpha)

# linear
def lerp(vector1, vector2, alpha):
    return (1 - alpha) * vector1 + alpha * vector2

for alpha in alpha_values:
    sample_interpolated = lerp(noise_vectors[0], noise_vectors[1], alpha)
    generate_image(model, sample_interpolated, alpha)